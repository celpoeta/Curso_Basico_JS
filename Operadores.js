// Operadores aritméticos
// + * - /
// + también concatena dos valores

3 + 2;
50 - 10;
10 * 20;
20 / 2;
"Caleb" + "Flores" 

// Operador de Asignación
// = += -= *= /=

a = 1; // a es igual 1
//  bar = 5
bar += 2; // 7
bar -= 2; // 3
bar *= 2; // 10
bar /= 2; // 2.5

// Operador Unitario
// !

!false;

// Operadores de Comparación
// == === < > <= >=

3 == "3"; // Aquí compara solo el valor

3 === "3"; // Aquí compara tanto como el valor y el tipo

5 < 3; // false
5 > 3; // true
5 <= 6; // true
5 >= 6; // false 

// Operadores Lógicos
// AND(&&) OR(||)
b && c; // Si ambos son igual a 1 se cumplirá la condición, si uno es diferente no se cumplirá la condición.
b || c; // Si a o b son igual que 1 se cumplirá la condición, si ambos no son 1 no se cumplirá.